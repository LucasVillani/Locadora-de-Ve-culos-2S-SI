    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        boolean loginValido = false;
        for (int i=0; i<Dados.numClientes;i++){
            if(Dados.clientes[i].validarLogin(login.getText(),senha.getText())){
            loginValido = true;
            Dados.usuarioLogado = Dados.clientes[i];
            }
        }
        if(loginValido){ 
        telaPrincipal tPrincipal = new telaPrincipal(Dados);
        tPrincipal.setVisible(true);
        dispose();
        }
        else{
        JOptionPane.showMessageDialog(rootPane,"Login ou senha invÃ¡lido");
        }
    }//GEN-LAST:event_entrarActionPerformed

